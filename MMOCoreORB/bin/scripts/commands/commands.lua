-- define an object to carry state data for combat commands
function StateEffect(t, et, dm, jm, c, s, l) 
  return {
	effectType = t,
	defenderExclusionTimers = et,
	defenderStateDefenseModifiers = dm,
	defenderJediStateDefenseModifiers = jm,
	stateChance = c,
	stateStrength = s,
	stateLength = l
  }
end

-- carries dot data for combat commands
function DotEffect(t, dm, pl, h, p, c, s, d)
  return {
	dotType = t,
	defenderStateDefenseModifers = dm,
	dotPool = pl,
	dotDamageofHit = h,
	dotPotency = p,
	dotChance = c,
	dotStrength = s,
	dotDuration = d
  }
end

